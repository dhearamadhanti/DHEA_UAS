
public class muncul {
	String username;
	String nim;
	
	public muncul(String username, String nim) {
		super();
		this.username = username;
		this.nim = nim;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getNim() {
		return nim;
	}

	public void setNim(String nim) {
		this.nim = nim;
	}
	
}
